func containsLine(line Line, lines []Line) bool {
	for _, l := range lines {
		if l == line {
			return true
		}
	}

	return false
}

func roughlyEqualFloats(a, b float64) bool {
	const equalityThreshold = 1e-7
	return math.Abs(a-b) < equalityThreshold
}

func roughlyEqualPoint(a, b Point) bool {
	return roughlyEqualFloats(a.X, b.X) &&
		roughlyEqualFloats(a.Y, b.Y)
}

func testName(t time.Time) string {
	return t.Format("15:04:05")
}

func simpleTime(hour, min, sec int) time.Time {
	return time.Date(1337, time.January, 0, hour, min, sec, 0, time.UTC)
}
